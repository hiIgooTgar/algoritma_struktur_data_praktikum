package igo_if24c_pratikum_3;

public class opeator_pembanding {

    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        
        System.out.println(a + " == " + b  + " : " + (a == b)); // Sama dengan
        System.out.println(a + " != " + b  + " : " + (a != b)); // Tidak sama dengan 
        System.out.println(a + " > " + b  + " : " + (a > b)); // Lebih dari
        System.out.println(a + " < " + b  + " : " + (a < b)); // Kurang dari
        System.out.println(a + " >= " + b  + " : " + (a >= b)); // Lebih dari sama dengan
        System.out.println(a + " <= " + b  + " : " + (a <= b)); // Kurang dari sama dengan
    }
    
}

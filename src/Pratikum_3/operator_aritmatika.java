
package igo_if24c_pratikum_3;

public class operator_aritmatika {

    public static void main(String[] args) {
      int a = 10;
      int b = 20;
      
        System.out.println(a + " + " + b + " = "+ (a+b) ); // Penjumlahan
        System.out.println(a + " - " + b + " = "+ (a-b) ); //Pengurangan
        System.out.println(a + " * " + b + " = "+ (a*b) ); //Perkalian
        System.out.println(a + " / " + b + " = "+ (a/b) ); //Pembagian
        System.out.println(a + " % " + b + " = "+ (a%b) ); //Modulus (Sisa bagi)
        System.out.println(a + " + " + 1 + " = "+ (++a) ); //Penambahan 1
        System.out.println(b + " - " + 1 + " = "+ (--b) ); // Pengurangan 1
        
    }
    
}

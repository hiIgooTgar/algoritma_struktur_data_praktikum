package igo_if24c_pratikum_3;

public class operator_bitwise {

    public static void main(String[] args) {
        int a = 5;
        int b = 3;
        
        System.out.println(a + " & " + b + " : " + (a & b));
        System.out.println(a + " | " + b + " : " + (a | b));
        System.out.println(a + " ^ " + b + " : " + (a ^ b));
        
    }
    
}

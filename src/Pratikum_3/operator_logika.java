package igo_if24c_pratikum_3;

public class operator_logika {

    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        
        System.out.println(a + " && " + b + (a && b)); // Logika AND
        System.out.println(a + " || " + b + (a || b)); // Logika OR
        System.out.println( "!" + a + " : " + (!a)); // Logika NOT
    }
    
}

package igo_praktikum_6;

public class loopingForFirst {

    public static void main(String[] args) {
        
        /* Misal nilai awalnya i = 0; kondisionalnya adalah i kurang dari 5;
        dan diiterasikan bertambahkan 1 / ++ increment*/
        for(int i = 0; i < 5; i++) {
            // Cetak perulangan sebanyak perintah kondisionalnya yaitu kurang dari 5
            System.out.println("Perulangan ke-" + i + " | For");
        }
    }
}

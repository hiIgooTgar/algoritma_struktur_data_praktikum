package igo_praktikum_6;

public class loopingForThrid {

    public static void main(String[] args) {
       /* Misal nilai awalnya i = 1; kondisionalnya adalah 
        i kurang dari sama dengan 5; dan diiterasikan bertambahkan 1 / ++ increment */
        for(int i = 1; i <= 5; i++) {
            // Cetak perulangan sebanyak perintah kondisionalnya yaitu 
            // kurang dari sama dengan 5
            System.out.println("Perulangan ke " + i + " | For");
        }
    }
}

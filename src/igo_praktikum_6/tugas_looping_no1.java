package igo_praktikum_6;

public class tugas_looping_no1 {

    public static void main(String[] args) {
        /* Misal nilai awalnya a = 1; kondisionalnya adalah a kurang 
        dari sama dengan 10; dan diiterasikan bertambahkan 1 / ++ increment */
        for(int a = 1; a <= 10; a++) {
            // Cetak perulangan sebanyak perintah kondisionalnya 
            // yaitu kurang dari sama dengan 10
            System.out.println(a);
        }
    }
}

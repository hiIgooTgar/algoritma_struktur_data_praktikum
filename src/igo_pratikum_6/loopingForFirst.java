package igo_pratikum_6;

public class loopingForFirst {

    public static void main(String[] args) {
        
        /* Misal nilai awalnya i = 0, kondisinya adalah i kurang dari 5; dan diiterasikan bertambahkan 1 */
        for(int i = 0; i < 5; i++) {
            // Cetak perulangan sebanyak perintah kondisionalnya yaitu 5
            System.out.println("Perulangan ke " + i + " For");
        }
    }
}

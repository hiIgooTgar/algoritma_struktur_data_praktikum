package igo_pratikum_6;

public class loopingForThrid {

    public static void main(String[] args) {
       /* Misal nilai awalnya i = 1, kondisinya adalah i kurang dari sama dengan 5; dan diiterasikan bertambahkan 1 */
        for(int i = 1; i <= 5; i++) {
            // Cetak perulangan sebanyak perintah kondisionalnya yaitu 5
            System.out.println("Perulangan ke " + i + " For");
        }
    }
}

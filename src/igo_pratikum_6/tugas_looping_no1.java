package igo_pratikum_6;

public class tugas_looping_no1 {

    public static void main(String[] args) {
        /* Misal nilai awalnya i = 1, kondisinya adalah i kurang dari sama dengan 10; dan diiterasikan bertambahkan 1 */
        for(int a = 1; a <= 10; a++) {
            // Cetak perulangan sebanyak perintah kondisionalnya yaitu 10
            System.out.println(a);
        }
    }
    
}
